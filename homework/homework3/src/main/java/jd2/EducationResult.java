package jd2;

public class EducationResult {

    private Double hoursAnalysis;
    private Double hoursPracticalPart;
    private Double hoursFlow;

    public Double getHoursAnalysis() {
        return hoursAnalysis;
    }

    public void setHoursAnalysis(Double hoursAnalysis) {
        this.hoursAnalysis = hoursAnalysis;
    }

    public Double getHoursPracticalPart() {
        return hoursPracticalPart;
    }

    public void setHoursPracticalPart(Double hoursPracticalPart) {
        this.hoursPracticalPart = hoursPracticalPart;
    }

    public Double getHoursFlow() {
        return hoursFlow;
    }

    public void setHoursFlow(Double hoursFlow) {
        this.hoursFlow = hoursFlow;
    }
}
